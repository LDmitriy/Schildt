
package ifTest;


public class IfTest {
  
    public static void main(String[] args) {
        for (int i = 0; i<12; i++){
            if (i>=10){
                System.out.println("i равно или больше 10");
            }
            else{
                if (i<5){
                    System.out.println("i меньше 5");
                }
            System.out.println("i меньше 10");
            }
        }
    }
    
}
